// using i++(Post-increment)
for(let i = 0; i < 9; i++){
  console.log(i);  // In this example the current value of i is logged, then i is incremented.
}

// Using ++i(Pre-increment)

for (let i = 0; i < 12; ++i){
  console.log(i); // In this second example i is incremented first, then the updated value is logged. though the two outputs are the same.
}